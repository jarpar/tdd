package a_junit;

public class Calculator {
    public Integer add(Integer a, Integer b) {
        return a + b;
    }

    Integer substract(Integer a, Integer b) {
        return a - b;
    }

    Integer multiply(Integer a, Integer b) {
        return a * b;
    }

    Integer divide(Integer a, Integer b) {
        return a / b;
    }
}
